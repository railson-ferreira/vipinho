package com.mineupserver.plugin.vipinho.utilidades;

public class Cordenadas {
	private double X;
	private double Y;
	private double Z;
	public Cordenadas(double x, double y, double z){
		X = x;
		Y = y;
		Z = z;
	}
	public double posX(){
		return X;
	}
	public double posY(){
		return Y;
	}
	public double posZ(){
		return Z;
	}
}
